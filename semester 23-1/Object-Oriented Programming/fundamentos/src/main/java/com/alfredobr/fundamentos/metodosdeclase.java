package com.alfredobr.fundamentos;

public class metodosdeclase {
    public static void main(String[] args){
        System.out.println(Math.pow(2,5));
        System.out.println(Math.sqrt(81));
        System.out.println(Math.E);
        System.out.println(Math.PI);
        System.out.println(Math.cbrt(8));
        System.out.println(Math.ceil(9.3));
        System.out.println(Math.floor(9.7));
        System.out.println(Math.abs(-5.93));
        System.out.println(Math.cos(0));
        System.out.println(Math.cos(Math.PI));
        System.out.println(Math.cos(2*Math.PI));
        System.out.println(Math.exp(2));
        System.out.println(Math.random());
        System.out.println(Math.round(8.5));
        System.out.println(Math.toRadians(180));
        System.out.println(Math.toDegrees(Math.PI));
        String team = new String ("England");
        System.out.println(team.replace('n', '@'));
        System.out.println(team.toUpperCase());
        
        
    }
}
